    private boolean validateInput()
    {
        if(txtUserId.getText().isEmpty()||txtPassword.getText().isEmpty()||txtRePassword.getText().isEmpty())
        {
            return false;
        }
        else
        {
            return true;
        }
    }
    private boolean passwordVerification()
    {
        if(txtPassword.getText().equals(txtRePassword.getText()))
            return true;
        else
            return false;
    }
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validateInput()==false)
        {
            JOptionPane.showMessageDialog(null, "Any Field cannot be left empty", "Empty Fields", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if(passwordVerification()==false)
        {
            JOptionPane.showMessageDialog(null, "Both the password fields did not match", "Match not found",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        User u=new User();
        u.setUserId(txtUserId.getText());
        u.setPassword(txtPassword.getText());
        String s=jcbEmpId.getSelectedItem().toString();
        String a=cashiers.get(jcbEmpId.getSelectedItem().toString());
        try
        {
            boolean b=UserDao.registerCashier(u, a, s);
            if(b==true)
            {
                JOptionPane.showMessageDialog(null, "Cashiers account successfully registered", "Registration Succeccful", JOptionPane.INFORMATION_MESSAGE);
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Cashier not registered due to some issue", "Error in regitration", JOptionPane.ERROR_MESSAGE);
                
            }
        txtUserId.setText("");
        txtPassword.setText("");
        txtRePassword.setText("");
        }
       
        
        catch(SQLException sq)
        {
            JOptionPane.showMessageDialog(null, "Cashier already registered", "Already registered   ", JOptionPane.INFORMATION_MESSAGE);
            sq.printStackTrace();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

